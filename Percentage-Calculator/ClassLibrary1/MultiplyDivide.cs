using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace MathLib
{
    public class MultiplyDivide
    {
        public Double Multiply(Double val1, Double val2)
        {
            return val1 * val2;
        }

        public Double Divide(Double val1, Double val2)
        {
            return val1 / val2;
        }
    }
}
